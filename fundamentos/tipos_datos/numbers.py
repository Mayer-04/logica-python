# Número entero - int
print(4)
# Número de punto flotante - float
print(2.5)
# complex
print(1j)

# Convertir un entero a un float con la función float()
print(float(2))
# Convertir un float a un entero con la función int()
print(int(2.5))
# Convertir un entero a un complex con la función complex()
print(complex(1))

# Se recomienda utilizar el guión bajo `_` para separar los miles en números grandes
print(1_000_000)
