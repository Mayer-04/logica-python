# Tipos numéricos en Python

# Número entero - tipo int
print(4)  # Salida: 4

# Número decimal o de punto flotante - tipo float
print(2.5)  # Salida: 2.5

# Número complejo - tipo complex
print(1j)  # Salida: 1j (parte imaginaria)

# Conversión de tipos numéricos

# Convertir un entero a un float
print(float(2))  # Salida: 2.0

# Convertir un float a un entero (se trunca, no redondea)
print(int(2.5))  # Salida: 2

# Convertir un entero a un número complejo
print(complex(1))  # Salida: (1+0j)

# Separación de miles con guiones bajos (para mejorar legibilidad)
print(1_000_000)  # Salida: 1000000
