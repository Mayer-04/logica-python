# =====================================
# * Función incorporada help()
# =====================================
# Muestra la documentación de Python sobre funciones, clases,
# métodos, módulos u objetos.
# Es útil para conocer cómo usar algo sin salir de Python.
#
# Forma general:
# help(objeto)

# Ejemplo 1: Obtener ayuda de una función incorporada
help(print)
# Muestra información sobre la función print(),
# sus parámetros y cómo se usa.

# Ejemplo 2: Obtener ayuda de un tipo de dato
help(str)  # Muestra la documentación de las cadenas de texto.

# Ejemplo 3: Ayuda general de Python
# Si se llama sin argumentos, abre el asistente interactivo de ayuda.
# (Escribe "quit" para salir de ese modo)
help()
