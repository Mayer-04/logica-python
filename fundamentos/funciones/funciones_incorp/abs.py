# =====================================
# * Función incorporada abs()
# =====================================
# La función abs() devuelve el valor absoluto de un número,
# es decir, su distancia respecto al cero sin importar el signo.
#
# Forma general:
# abs(numero)
#
# Parámetro:
# - numero: puede ser entero, flotante o un número complejo.

# Ejemplo 1: Número entero negativo
print(abs(-5))  # Salida: 5

# Ejemplo 2: Número entero positivo
print(abs(7))  # Salida: 7

# Ejemplo 3: Número decimal
print(abs(-3.14))  # Salida: 3.14

# Ejemplo 4: Número complejo
# El valor absoluto de un número complejo es su módulo.
print(abs(3 + 4j)) # Salida: 5.0  → porque √(3² + 4²) = 5
