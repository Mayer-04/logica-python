"""
* Operadores aritméticos
-------------------------
Los operadores aritméticos nos permiten realizar operaciones matemáticas con variables y valores.

Algunos de los más comunes son:
- `+` (suma): se usa para sumar números. También puede usarse para unir (concatenar) cadenas de texto.
- `-` (resta): se usa para restar un valor a otro.
- `*` (multiplicación): sirve para multiplicar números. También se puede usar para repetir cadenas de texto.
- `/` (división): divide un valor entre otro y devuelve un número decimal.
- `//` (división entera): divide un valor entre otro y devuelve el resultado redondeado a un número entero.

Ejemplos:
---------
1. Suma de números:
   resultado = 2 + 3  # resultado es: 5

2. Concatenación de texto:
   saludo = "Hola" + " mundo"  # saludo es: "Hola mundo"

3. Repetición de texto:
   eco = "hey! " * 3  # eco es: "hey! hey! hey! "
"""

# Suma
print(2 + 2)

# Resta
print(2 - 2)

# Multiplicación
print(2 * 2)

# División: Devuelve un número flotante
print(2 / 5)  # Salida: 0.4

# División entera: Redondea el resultado a un número entero, descarta la parte decimal
print(2 // 2)  # Salida: 1

# Potencia
print(2**2)

# Módulo o residuo
print(2 % 2)  # Salida: 0

# Concatenación
print("2" + "2")  # Salida: "22"

# Repetición
print("1" * 3)  # Salida: "111"
