"""
* Operadores aritméticos
------------------------
- Realizan operaciones matemáticas en variables y valores.
- El operador `+` (suma) también nos ayuda a concatenar cadenas o variables.
- El operador `*` (multiplicación) también nos ayuda a repetir una cadena o variable.
"""

# Suma
print(2 + 2)
# Resta
print(2 - 2)
# Multiplicación
print(2 * 2)
# División - Devuelve un número flotante
print(2 / 5)
# División entera - Redondea el resultado a un número entero, descarte la parte decimal
print(2 // 2)
# Potencia
print(2**2)
# Módulo o residuo
print(2 % 2)
